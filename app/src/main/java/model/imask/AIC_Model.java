public class AIC_Model {

    private String compName;
    private String compVenue;
    private String compYear;
    private String compDate;
    private String compID;


    public String getCompID() {
        return compID;
    }

    public void setCompID(String compID) {
        this.compID = compID;
    }

    public String getCompName() {
        return compName;
    }

    public void setCompName(String compName) {
        this.compName = compName;
    }

    public String getCompVenue() {
        return compVenue;
    }

    public void setCompVenue(String compVenue) {
        this.compVenue = compVenue;
    }

    public String getCompYear() {
        return compYear;
    }

    public void setCompYear(String compYear) {
        this.compYear = compYear;
    }

    public String getCompDate() {
        return compDate;
    }

    public void setCompDate(String compDate) {
        this.compDate = compDate;
    }
}
